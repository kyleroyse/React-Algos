function anagrams(word, words) {
    let anagrams = [];
    word.split('').sort().forEach(letter => {
        if (letter === words.forEach(word => {
            word.split('').sort();
        })) {
            anagrams.push(word[word]);
        }
    });
    return anagrams;
}

export default anagrams;

